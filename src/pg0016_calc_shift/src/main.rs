// . メイン関数

fn main() {
    let a:u8=0b1001;
    
    let mut ans:u8=0;

    ans=a<<1;
    println!("{:0>8b}の1ビット左シフト={:0>8b}",a,ans);
    ans=a<<2;
    println!("{:0>8b}の2ビット左シフト={:0>8b}",a,ans);
    ans=a>>1;
    println!("{:0>8b}の1ビット右シフト={:0>8b}",a,ans);
    ans=a>>2;
    println!("{:0>8b}の2ビット右シフト={:0>8b}",a,ans);



}
