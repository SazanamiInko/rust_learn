/// . メイン関数
fn main() {

    //変更可能な変数として宣言する
    let mut buf=0;
    let mut a=0;
    let mut b=1;
   
    //変数の内容を入れ替えます・
    println!("変数の内容を入れ替えます");
    println!("a={},b={}",a,b);
    
    //退避
    buf=a;
    //入れ替え
    a=b;
    b=buf;
   
    println!("a={},b={}",a,b);
   }
   