// . メイン関数

fn main() {

    let a:u8=0b1001;
    let b:u8=0b0111;
    let mut ans:u8=0;
 
    ans=a|b;
    println!("{:0>8b}と{:0>8b}の論理和={:0>8b}",a,b,ans);
 
    ans=a&b;
    println!("{:0>8b}と{:0>8b}の論理積={:0>8b}",a,b,ans);
 
    ans=a^b;
     println!("{:0>8b}と{:0>8b}の排他論理積={:0>8b}",a,b,ans);
 
    ans=!a;
    println!("{:0>8b}の否定=={:0>8b}",a,ans);
 
 }
 